package org.example.oop.methods.domain;

public class Calculator {
    public int sumTwoNumbers(int num1, int num2) {
        int result = num1 + num2;

        return result;
    }

    public int subtractTwoNumbers(int num1, int num2) {
    
        int result = num1 - num2;

        return result;
    }   


    public int multiplyTwoNumber(int num1, int num2) {
    
        int result = num1 * num2;

        return result;
    }   
}
